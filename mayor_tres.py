#ejemplo de max

primer_numero = int(input("primer numero : "))
segundo_numero = int(input("segundo numero : "))
tercero_numero = int(input("tercer numero : ")) #estas son las variables

resultado = max(primer_numero, segundo_numero, tercero_numero)

print("el numero mas grande es {}".format(resultado)) #donde pone resutado entre () es la variable resultado = max

